public class Product {
    String title;
    String price;
    String brand;
    String desc;
    String imagePath;

    public Product(String title, String price, String brand, String desc, String imagePath) {
        this.title = title;
        this.price = price;
        this.brand = brand;
        this.desc = desc;
        this.imagePath = imagePath;
    }
}